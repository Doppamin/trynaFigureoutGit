//------------------------------------------------------------------------------
    public static char[][] operation(char[][] board, char key,char[] brush,int[] cor ){
        switch(key){
            case 'C':select(brush);break; //change brush
            case 'P':paint(brush,board,cor);break;
            case 'E':erase(brush,board,cor);break;
            default:                       
        }
        return board;
    }
      
//------------------------------------------------------------------------------
public static char[][]paint(char[] brush,char[][]board,int[] cor){
    
    board[cor[0]][cor[1]]=brush[0];
    
    return board;
}
//------------------------------------------------------------------------------
public static char[][]erase(char[] brush,char[][]board,int[] cor){
    
    board[cor[0]][cor[1]]=brush[brush.length-1];
    
    return board;
}
//------------------------------------------------------------------------------
public static char[] select(char[] brush){
    char temp= brush[0];
    
    for (int i = 0; i <brush.length-2 ; i++) {
        
        brush[i]=brush[i+1];
    }
    brush[brush.length-2]=temp;
    return brush ;
}
  
